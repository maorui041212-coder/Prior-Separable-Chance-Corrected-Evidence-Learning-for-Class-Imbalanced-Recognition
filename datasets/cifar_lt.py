"""
CIFAR-LT dataset builders for CCEL-Net.

Supported:
    - CIFAR-10-LT
    - CIFAR-100-LT

This file does not change the project structure.
It only provides long-tailed CIFAR datasets under ccel.datasets.cifar_lt.

The training set provides:
    - class_counts
    - num_classes
    - get_class_counts_tensor()

These statistics should be shared by CE variants, Re-weighting, LDAM,
Class-Balanced Loss, Balanced Softmax, Logit Adjustment, MICELoss,
and CCEL-Net.
"""

from __future__ import annotations

import json
import random
from pathlib import Path
from typing import Dict, List, Literal, Sequence, Tuple

import numpy as np
from PIL import Image
from ccel.utils.seed import seed_worker, build_generator
import torch
from torch.utils.data import DataLoader, Dataset
from torchvision import datasets, transforms


CIFAR10_MEAN = (0.4914, 0.4822, 0.4465)
CIFAR10_STD = (0.2470, 0.2435, 0.2616)

CIFAR100_MEAN = (0.5071, 0.4867, 0.4408)
CIFAR100_STD = (0.2675, 0.2565, 0.2761)


def get_cifar_transform(
    dataset_name: Literal["cifar10", "cifar100"],
    train: bool,
    image_size: int = 32,
):
    """
    CIFAR default image size is 32.

    If your ResNet50 has CIFAR-style stem:
        image_size=32

    If your ResNet50 is original ImageNet-style torchvision ResNet50:
        image_size=224 can be used for a temporary smoke test,
        but it is not the recommended final setting.
    """
    dataset_name = dataset_name.lower()

    if dataset_name == "cifar10":
        mean, std = CIFAR10_MEAN, CIFAR10_STD
    elif dataset_name == "cifar100":
        mean, std = CIFAR100_MEAN, CIFAR100_STD
    else:
        raise ValueError(f"Unknown dataset_name: {dataset_name}")

    if image_size == 32:
        if train:
            return transforms.Compose(
                [
                    transforms.RandomCrop(32, padding=4),
                    transforms.RandomHorizontalFlip(),
                    transforms.ToTensor(),
                    transforms.Normalize(mean, std),
                ]
            )
        return transforms.Compose(
            [
                transforms.ToTensor(),
                transforms.Normalize(mean, std),
            ]
        )

    if image_size > 32:
        if train:
            return transforms.Compose(
                [
                    transforms.Resize((image_size, image_size)),
                    transforms.RandomHorizontalFlip(),
                    transforms.ToTensor(),
                    transforms.Normalize(mean, std),
                ]
            )
        return transforms.Compose(
            [
                transforms.Resize((image_size, image_size)),
                transforms.ToTensor(),
                transforms.Normalize(mean, std),
            ]
        )

    raise ValueError(f"image_size must be >= 32, got {image_size}")


class CIFARLT(Dataset):
    """
    Long-tailed CIFAR dataset.

    CIFAR original training set is class-balanced.
    CIFAR-LT is generated by sampling different numbers of images
    from each class according to an exponential long-tailed rule.

    imbalance_factor:
        max_class_count / min_class_count

    Example:
        CIFAR-10-LT IF=100:
            head class: about 5000 images before val split
            tail class: about 50 images before val split

        CIFAR-100-LT IF=100:
            head class: about 500 images before val split
            tail class: about 5 images before val split
    """

    def __init__(
        self,
        root: str | Path,
        dataset_name: Literal["cifar10", "cifar100"],
        split: Literal["train", "val", "test"],
        imbalance_factor: int = 100,
        val_ratio: float = 0.1,
        seed: int = 2024,
        image_size: int = 32,
        download: bool = True,
        transform=None,
    ):
        super().__init__()

        self.root = Path(root)
        self.dataset_name = dataset_name.lower()
        self.split = split
        self.imbalance_factor = int(imbalance_factor)
        self.val_ratio = float(val_ratio)
        self.seed = int(seed)

        if self.dataset_name == "cifar10":
            dataset_cls = datasets.CIFAR10
            self.num_classes = 10
        elif self.dataset_name == "cifar100":
            dataset_cls = datasets.CIFAR100
            self.num_classes = 100
        else:
            raise ValueError(
                f"dataset_name must be 'cifar10' or 'cifar100', got {dataset_name}"
            )

        if split in {"train", "val"}:
            self.base_dataset = dataset_cls(
                root=str(self.root),
                train=True,
                download=download,
                transform=None,
            )

            base_targets = np.asarray(self.base_dataset.targets, dtype=np.int64)

            lt_indices, planned_counts = self._make_long_tail_indices(
                targets=base_targets,
                num_classes=self.num_classes,
                imbalance_factor=self.imbalance_factor,
                seed=self.seed,
            )

            train_indices, val_indices = self._stratified_train_val_split(
                indices=lt_indices,
                targets=base_targets,
                num_classes=self.num_classes,
                val_ratio=self.val_ratio,
                seed=self.seed,
            )

            if split == "train":
                self.indices = train_indices
            else:
                self.indices = val_indices

            self.planned_counts = planned_counts

        elif split == "test":
            self.base_dataset = dataset_cls(
                root=str(self.root),
                train=False,
                download=download,
                transform=None,
            )
            self.indices = list(range(len(self.base_dataset)))
            self.planned_counts = None

        else:
            raise ValueError(f"split must be train/val/test, got {split}")

        if transform is None:
            self.transform = get_cifar_transform(
                dataset_name=self.dataset_name,
                train=(split == "train"),
                image_size=image_size,
            )
        else:
            self.transform = transform

        self.targets = [
            int(self.base_dataset.targets[idx]) for idx in self.indices
        ]

        self.class_counts = self._count_classes(
            targets=self.targets,
            num_classes=self.num_classes,
        )

    @staticmethod
    def _count_classes(
        targets: Sequence[int],
        num_classes: int,
    ) -> List[int]:
        counts = [0 for _ in range(num_classes)]
        for y in targets:
            counts[int(y)] += 1
        return counts

    @staticmethod
    def _get_img_num_per_cls(
        num_classes: int,
        max_num: int,
        imbalance_factor: int,
    ) -> List[int]:
        """
        Exponential long-tailed sampling rule:

            n_i = n_max * (1 / IF) ** (i / (C - 1))

        Class 0 is the most frequent class.
        Class C-1 is the least frequent class.
        """
        img_num_per_cls = []

        for cls_idx in range(num_classes):
            exponent = cls_idx / max(1, num_classes - 1)
            num = max_num * ((1.0 / imbalance_factor) ** exponent)
            img_num_per_cls.append(int(num))

        return img_num_per_cls

    @classmethod
    def _make_long_tail_indices(
        cls,
        targets: np.ndarray,
        num_classes: int,
        imbalance_factor: int,
        seed: int,
    ) -> Tuple[List[int], List[int]]:
        rng = np.random.default_rng(seed)

        max_num = int(len(targets) / num_classes)

        img_num_per_cls = cls._get_img_num_per_cls(
            num_classes=num_classes,
            max_num=max_num,
            imbalance_factor=imbalance_factor,
        )

        selected_indices: List[int] = []

        for class_id, num in enumerate(img_num_per_cls):
            cls_indices = np.where(targets == class_id)[0]
            rng.shuffle(cls_indices)

            num = min(num, len(cls_indices))
            selected_indices.extend(cls_indices[:num].tolist())

        rng.shuffle(selected_indices)

        return selected_indices, img_num_per_cls

    @staticmethod
    def _stratified_train_val_split(
        indices: Sequence[int],
        targets: np.ndarray,
        num_classes: int,
        val_ratio: float,
        seed: int,
    ) -> Tuple[List[int], List[int]]:
        if val_ratio <= 0:
            return list(indices), []

        rng = random.Random(seed)
        by_class: Dict[int, List[int]] = {c: [] for c in range(num_classes)}

        for idx in indices:
            y = int(targets[idx])
            by_class[y].append(int(idx))

        train_indices: List[int] = []
        val_indices: List[int] = []

        for c in range(num_classes):
            cls_indices = by_class[c]
            rng.shuffle(cls_indices)

            if len(cls_indices) <= 1:
                train_indices.extend(cls_indices)
                continue

            n_val = max(1, int(round(len(cls_indices) * val_ratio)))
            n_val = min(n_val, len(cls_indices) - 1)

            val_indices.extend(cls_indices[:n_val])
            train_indices.extend(cls_indices[n_val:])

        rng.shuffle(train_indices)
        rng.shuffle(val_indices)

        return train_indices, val_indices

    def __len__(self) -> int:
        return len(self.indices)

    def __getitem__(self, index: int):
        real_index = self.indices[index]

        img = self.base_dataset.data[real_index]
        target = int(self.base_dataset.targets[real_index])

        img = Image.fromarray(img)

        if self.transform is not None:
            img = self.transform(img)

        return img, target

    def get_class_counts_tensor(self) -> torch.Tensor:
        return torch.tensor(self.class_counts, dtype=torch.float32)

    def get_class_priors_tensor(self) -> torch.Tensor:
        counts = self.get_class_counts_tensor()
        return counts / counts.sum().clamp_min(1.0)

    def save_meta(self, path: str | Path):
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)

        meta = {
            "dataset_name": self.dataset_name,
            "split": self.split,
            "num_classes": self.num_classes,
            "imbalance_factor": self.imbalance_factor,
            "val_ratio": self.val_ratio,
            "seed": self.seed,
            "num_samples": len(self),
            "class_counts": self.class_counts,
            "planned_long_tail_counts_before_val_split": self.planned_counts,
        }

        with open(path, "w", encoding="utf-8") as f:
            json.dump(meta, f, indent=2, ensure_ascii=False)


def build_cifar_lt_datasets(
    root: str | Path,
    dataset_name: Literal["cifar10", "cifar100"],
    imbalance_factor: int = 100,
    val_ratio: float = 0.1,
    seed: int = 2024,
    image_size: int = 32,
    download: bool = True,
):
    train_set = CIFARLT(
        root=root,
        dataset_name=dataset_name,
        split="train",
        imbalance_factor=imbalance_factor,
        val_ratio=val_ratio,
        seed=seed,
        image_size=image_size,
        download=download,
    )

    val_set = CIFARLT(
        root=root,
        dataset_name=dataset_name,
        split="val",
        imbalance_factor=imbalance_factor,
        val_ratio=val_ratio,
        seed=seed,
        image_size=image_size,
        download=download,
    )

    test_set = CIFARLT(
        root=root,
        dataset_name=dataset_name,
        split="test",
        imbalance_factor=imbalance_factor,
        val_ratio=val_ratio,
        seed=seed,
        image_size=image_size,
        download=download,
    )

    return train_set, val_set, test_set


def build_cifar_lt_loaders(
    root: str | Path,
    dataset_name: Literal["cifar10", "cifar100"],
    imbalance_factor: int = 100,
    val_ratio: float = 0.1,
    seed: int = 2024,
    image_size: int = 32,
    batch_size: int = 128,
    num_workers: int = 4,
    download: bool = True,
):
    train_set, val_set, test_set = build_cifar_lt_datasets(
        root=root,
        dataset_name=dataset_name,
        imbalance_factor=imbalance_factor,
        val_ratio=val_ratio,
        seed=seed,
        image_size=image_size,
        download=download,
    )
    generator = build_generator(seed)

    train_loader = DataLoader(
        train_set,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=True,
        drop_last=True,
        worker_init_fn=seed_worker,
        generator=generator,
    )

    val_loader = DataLoader(
        val_set,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
        drop_last=False,
        worker_init_fn=seed_worker,
        generator=generator,
    )

    test_loader = DataLoader(
        test_set,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True,
        drop_last=False,
        worker_init_fn=seed_worker,
        generator=generator,
    )

    return train_loader, val_loader, test_loader, train_set, val_set, test_set


def print_dataset_summary(dataset: CIFARLT):
    print("=" * 80)
    print(f"Dataset: {dataset.dataset_name.upper()}-LT")
    print(f"Split: {dataset.split}")
    print(f"Num classes: {dataset.num_classes}")
    print(f"Imbalance factor: {dataset.imbalance_factor}")
    print(f"Num samples: {len(dataset)}")
    print(f"Max class count: {max(dataset.class_counts)}")
    print(f"Min class count: {min(dataset.class_counts)}")
    print("Class counts:")
    print(dataset.class_counts)